

fun main(args: Array<String>) {
    //var merupakan  sebuah variabel yang nilainya bisa diubah dan kebalikannya adalah val(konstanta)
    var dynamicVar = 2
    println("Contoh dynamic variabel = $dynamicVar")

//val adalah variabel yang nilainya adalah konstanta dan tidak bisa diubah
    val maxFloat: Float = 9.123456789f
    val maxDouble: Double = 9.123456789
    println("maxFloat:" + maxFloat)
    println("maxDouble:" + maxDouble)
}